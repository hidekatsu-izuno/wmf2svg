	private SymbolFontMappings() {
	}

	public static boolean isMappedFont(String faceName) {
		return getMapping(faceName) != null;
	}

	public static String replace(String faceName, String text) {
		int[] mapping = getMapping(faceName);
		if (mapping == null || text == null || text.length() == 0) {
			return text;
		}

		StringBuilder buffer = new StringBuilder(text.length());
		for (int i = 0; i < text.length(); i++) {
			char ch = text.charAt(i);
			int mapped = (ch <= 0xFF) ? mapping[ch] : 0;
			if (mapped != 0) {
				buffer.appendCodePoint(mapped);
			} else {
				buffer.append(ch);
			}
		}
		return buffer.toString();
	}

	private static int[] getMapping(String faceName) {
		if (faceName == null) {
			return null;
		}
		String key = faceName.toLowerCase(Locale.ENGLISH);
		if ("symbol".equals(key)) {
			return SYMBOL;
		} else if ("wingdings".equals(key)) {
			return WINGDINGS;
		} else if ("wingdings 2".equals(key)) {
			return WINGDINGS_2;
		} else if ("wingdings 3".equals(key)) {
			return WINGDINGS_3;
		}
		return null;
	}

